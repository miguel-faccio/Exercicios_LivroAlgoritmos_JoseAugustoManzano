// Ler quatro valores numéricos inteiros e apresentar os resultados das adições e multiplicações dos quatro números ente si.
void main(){
    double prim = 2;
    double secu = 4;
    double terc = 6;
    double quar = 8;
    print("$prim , $secu , $terc , $quar");
    double soma = prim + secu + terc + quar;
    print(soma);
    double multi = prim * secu * terc * quar;
    print(multi);
}